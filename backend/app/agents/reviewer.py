class PayloadReview(BaseModel):
    """Complete review of a payload."""

    review_id: str = Field(default_factory=lambda: str(uuid4()))
    payload_id: str = Field(..., description="ID of payload being reviewed")
    session_id: str = Field(...)

    # Review metadata
    status: ReviewStatus = ReviewStatus.PENDING
    reviewed_at: datetime = Field(default_factory=datetime.utcnow)
    reviewer_agent: str = Field(default="reviewer")
    review_version: str = "1.0.0"

    # Payload information
    payload_content: str = Field(..., description="The payload content")
    payload_category: str = Field(..., description="OWASP LLM category")
    stealth_technique: Optional[str] = Field(None, description="Stealth technique used")

    # Validation results
    is_valid: bool = Field(default=False, description="Whether payload passed validation")
    validation_errors: List[str] = Field(default_factory=list)
    validation_warnings: List[str] = Field(default_factory=list)

    # Quality assessment
    stealth_assessment: Optional[StealthAssessment] = None
    correctness_score: float = Field(default=0.0, ge=0.0, le=100.0)
    completeness_score: float = Field(default=0.0, ge=0.0, le=100.0)

    # Issues found
    issues: List[ReviewIssue] = Field(default_factory=list)
    critical_issues_count: int = Field(default=0)
    high_issues_count: int = Field(default=0)

    # Compliance checks
    compliance_checks: List[ComplianceCheck] = Field(default_factory=list)
    all_compliance_passed: bool = Field(default=True)

    # Recommendations
    approval_decision: str = Field(default="", description="Approved/Rejected/Needs Revision")
    approval_reasoning: str = Field(default="", description="Explanation of decision")
    improvement_recommendations: List[str] = Field(default_factory=list)

    # Review metrics
    review_duration_seconds: Optional[float] = Field(None)
    llm_tokens_used: int = Field(default=0)

    def calculate_scores(self) -> None:
        """Calculate aggregated scores from review data."""
        # Count issues by severity
        self.critical_issues_count = sum(
            1 for issue in self.issues if issue.severity == SeverityLevel.CRITICAL
        )
        self.high_issues_count = sum(
            1 for issue in self.issues if issue.severity == SeverityLevel.HIGH
        )

        # Calculate correctness based on issues
        if self.issues:
            severity_weights = {
                SeverityLevel.CRITICAL: 50,
                SeverityLevel.HIGH: 25,
                SeverityLevel.MEDIUM: 10,
                SeverityLevel.LOW: 5,
                SeverityLevel.INFO: 0,
            }
            penalty = sum(
                severity_weights.get(issue.severity, 0) for issue in self.issues
            )
            self.correctness_score = max(0, 100 - penalty)
        else:
            self.correctness_score = 100.0

        # Determine validation status
        self.is_valid = (
            self.critical_issues_count == 0 and
            self.correctness_score >= 50.0 and
            self.all_compliance_passed
        )

    def determine_approval(self) -> None:
        """Determine approval decision based on review results."""
        if self.critical_issues_count > 0:
            self.approval_decision = "REJECTED"
            self.status = ReviewStatus.REJECTED
            self.approval_reasoning = (
                f"Payload rejected due to {self.critical_issues_count} critical issue(s)"
            )
        elif self.high_issues_count >= 3:
            self.approval_decision = "NEEDS_REVISION"
            self.status = ReviewStatus.NEEDS_REVISION
            self.approval_reasoning = (
                f"Payload needs revision due to {self.high_issues_count} high-severity issues"
            )
        elif not self.all_compliance_passed:
            self.approval_decision = "REJECTED"
            self.status = ReviewStatus.REJECTED
            self.approval_reasoning = "Payload rejected due to compliance violations"
        elif self.correctness_score >= 80:
            self.approval_decision = "APPROVED"
            self.status = ReviewStatus.APPROVED
            self.approval_reasoning = "Payload approved with high quality score"
        elif self.correctness_score >= 60:
            self.approval_decision = "APPROVED_WITH_WARNINGS"
            self.status = ReviewStatus.APPROVED_WITH_WARNINGS
            self.approval_reasoning = (
                "Payload approved with warnings - minor issues found"
            )
        else:
            self.approval_decision = "NEEDS_REVISION"
            self.status = ReviewStatus.NEEDS_REVISION
            self.approval_reasoning = (
                f"Payload needs revision - correctness score too low ({self.correctness_score:.1f}/100)"
            )


class BatchReviewResult(BaseModel):
    """Result of reviewing multiple payloads."""

    batch_id: str = Field(default_factory=lambda: str(uuid4()))
    session_id: str
    total_payloads: int
    approved_count: int = 0
    approved_with_warnings_count: int = 0
    needs_revision_count: int = 0
    rejected_count: int = 0
    pending_count: int = 0

    individual_reviews: List[PayloadReview] = Field(default_factory=list)

    batch_approval_decision: str = Field(default="")
    batch_summary: str = Field(default="")

    def calculate_summary(self) -> None:
        """Calculate batch summary statistics."""
        self.approved_count = sum(
            1 for r in self.individual_reviews if r.status == ReviewStatus.APPROVED
        )
        self.approved_with_warnings_count = sum(
            1 for r in self.individual_reviews if r.status == ReviewStatus.APPROVED_WITH_WARNINGS
        )
        self.needs_revision_count = sum(
            1 for r in self.individual_reviews if r.status == ReviewStatus.NEEDS_REVISION
        )
        self.rejected_count = sum(
            1 for r in self.individual_reviews if r.status == ReviewStatus.REJECTED
        )
        self.pending_count = sum(
            1 for r in self.individual_reviews if r.status == ReviewStatus.PENDING
        )

        # Batch approval if all approved or approved with warnings
        if self.rejected_count > 0:
            self.batch_approval_decision = "REJECTED"
            self.batch_summary = (
                f"Batch rejected: {self.rejected_count} payload(s) rejected"
            )
        elif self.needs_revision_count > 0:
            self.batch_approval_decision = "NEEDS_REVISION"
            self.batch_summary = (
                f"Batch needs revision: {self.needs_revision_count} payload(s) require revision"
            )
        else:
            self.batch_approval_decision = "APPROVED"
            self.batch_summary = (
                f"Batch approved: {self.approved_count} approved, "
                f"{self.approved_with_warnings_count} approved with warnings"
            )


