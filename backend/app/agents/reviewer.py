    def __init__(self, config: Optional[AgentConfig] = None):
        """
        Initialize the Reviewer Agent.

        Args:
            config: Optional agent configuration. Uses defaults if not provided.
        """
        if config is None:
            config = AgentConfig(
                name="reviewer",
                role=AgentRole.REVIEWER,
                model_alias="reviewer-agent",
                temperature=0.2,  # Very low temperature for consistent evaluation
                max_tokens=4096,
                enable_long_term_memory=True,
                memory_collection="reviewer_memories",
            )
        super().__init__(config)

        # Initialize A2A protocol adapter for inter-agent communication
        self._a2a = A2AProtocolAdapter(
            config=A2AConfig(
                agent_name="reviewer",
                agent_role="reviewer",
                agent_version="1.0.0",
            ),
        )

        # Register A2A message handlers
        self._register_a2a_handlers()

        logger.info("ReviewerAgent initialized with A2A protocol support")

    def _register_a2a_handlers(self) -> None:
        """Register A2A protocol message handlers."""
        self._a2a_handlers = {
            "review_payload": self._handle_review_payload,
            "validate_payload": self._handle_validate_payload,
            "assess_stealth": self._handle_assess_stealth,
            "check_compliance": self._handle_check_compliance,
            "batch_review": self._handle_batch_review,
            "get_review": self._handle_get_review,
            "health_check": self._handle_health_check,
        }

    async def process(
        self,
        context: AgentContext,
        input_data: Dict[str, Any],
    ) -> AgentResponse:
        """
        Process the agent's main task.

        Routes to appropriate handler based on input data.

        Args:
            context: Agent context with session ID, shared data, conversation history
            input_data: Structured input data containing task type and parameters

        Returns:
            Agent response with structured results
        """
        start_time = datetime.utcnow()
        task_type = input_data.get("task_type", "unknown")

        try:
            # Route to appropriate handler
            if task_type == "review_payload":
                result = await self.review_payload(
                    context=context,
                    payload_data=input_data.get("payload_data"),
                    constraints=input_data.get("constraints", {}),
                    scope_limitations=input_data.get("scope_limitations", []),
                )
                response_content = json.dumps(result.dict(), indent=2, default=str)

            elif task_type == "validate_payload":
                result = await self.validate_payload(
                    context=context,
                    payload_content=input_data.get("payload_content"),
                    expected_category=input_data.get("expected_category"),
                )
                response_content = json.dumps(result, indent=2, default=str)

            elif task_type == "assess_stealth":
                result = await self.assess_stealth(
                    context=context,
                    payload_content=input_data.get("payload_content"),
                    stealth_technique=input_data.get("stealth_technique"),
                    visible_content=input_data.get("visible_content"),
                )
                response_content = json.dumps(result.dict(), indent=2, default=str)

            elif task_type == "check_compliance":
                result = await self.check_compliance(
                    context=context,
                    payload_data=input_data.get("payload_data"),
                    constraints=input_data.get("constraints", []),
                    scope_limitations=input_data.get("scope_limitations", []),
                )
                response_content = json.dumps(result, indent=2, default=str)

            elif task_type == "batch_review":
                result = await self.batch_review(
                    context=context,
                    payloads=input_data.get("payloads", []),
                    constraints=input_data.get("constraints", {}),
                    scope_limitations=input_data.get("scope_limitations", []),
                )
                response_content = json.dumps(result.dict(), indent=2, default=str)

            elif task_type == "get_review":
                result = await self.get_review(
                    context=context,
                    review_id=input_data.get("review_id"),
                )
                if result:
                    response_content = json.dumps(result.dict(), indent=2, default=str)
                else:
                    response_content = '{"error": "Review not found"}'

            elif task_type == "list_reviews":
                result = await self.list_reviews(context=context)
                response_content = json.dumps(
                    [r.dict() if hasattr(r, "dict") else r for r in result],
                    indent=2,
                    default=str
                )

            else:
                # Unknown task type - provide help
                response_content = self._get_help_text()

            # Calculate processing time
            processing_time = (datetime.utcnow() - start_time).total_seconds()

            # Store result in memory
            await self.save_to_memory(
                key=f"last_result_{task_type}",
                value=response_content,
                context=context,
            )

            return AgentResponse(
                agent_name=self.config.name,
                agent_role=self.config.role,
                content=response_content,
                state=AgentState.COMPLETED,
                metadata={
                    "task_type": task_type,
                    "processing_time_seconds": processing_time,
                },
            )

        except Exception as e:
            logger.error(f"ReviewerAgent processing failed: {e}", exc_info=True)
            return AgentResponse(
                agent_name=self.config.name,
                agent_role=self.config.role,
                content="",
                state=AgentState.FAILED,
                error=str(e),
            )

    async def review_payload(
        self,
        context: AgentContext,
        payload_data: Dict[str, Any],
        constraints: List[str],
        scope_limitations: List[str],
    ) -> PayloadReview:
        """
        Perform a complete review of a payload.

        Args:
            context: Agent context
            payload_data: Payload to review
            constraints: Testing constraints
            scope_limitations: Out-of-scope items

        Returns:
            Complete PayloadReview with all assessments
        """
        start_time = datetime.utcnow()

        # Extract payload info
        payload_id = payload_data.get("payload_id", str(uuid.uuid4()))
        payload_content = payload_data.get("content", "")
        payload_category = payload_data.get("category", "Unknown")
        stealth_technique = payload_data.get("stealth_technique")

        # Create review object
        review = PayloadReview(
            payload_id=payload_id,
            session_id=context.session_id,
            payload_content=payload_content,
            payload_category=payload_category,
            stealth_technique=stealth_technique,
        )

        # Step 1: Validate payload
        validation_result = await self._validate_payload_internal(
            payload_content=payload_content,
            expected_category=payload_category,
            context=context,
        )
        review.is_valid = validation_result["is_valid"]
        review.validation_errors = validation_result.get("validation_errors", [])
        review.validation_warnings = validation_result.get("validation_warnings", [])
        review.correctness_score = validation_result.get("correctness_score", 0.0)
        review.completeness_score = validation_result.get("completeness_score", 0.0)

        # Step 2: Assess stealth
        stealth_result = await self._assess_stealth_internal(
            payload_content=payload_content,
            stealth_technique=stealth_technique,
            visible_content=payload_data.get("visible_content"),
            context=context,
        )
        review.stealth_assessment = StealthAssessment(**stealth_result)

        # Step 3: Detect issues
        issues_result = await self._detect_issues_internal(
            payload_content=payload_content,
            payload_category=payload_category,
            context=context,
        )
        for issue_data in issues_result.get("issues", []):
            review.issues.append(ReviewIssue(
                severity=SeverityLevel(issue_data.get("severity", "info")),
                category=issue_data.get("category", "general"),
                title=issue_data.get("title", "Issue"),
                description=issue_data.get("description", ""),
                location=issue_data.get("location"),
                suggestion=issue_data.get("suggestion"),
            ))

        # Step 4: Check compliance
        compliance_result = await self._check_compliance_internal(
            payload_content=payload_content,
            payload_data=payload_data,
            constraints=constraints,
            scope_limitations=scope_limitations,
            context=context,
        )
        for check_data in compliance_result.get("compliance_checks", []):
            review.compliance_checks.append(ComplianceCheck(**check_data))
        review.all_compliance_passed = compliance_result.get("all_passed", True)

        # Step 5: Calculate scores and determine approval
        review.calculate_scores()
        review.determine_approval()

        # Step 6: Generate improvement recommendations
        review.improvement_recommendations = await self._generate_recommendations(
            review=review,
            context=context,
        )

        # Record timing
        review.review_duration_seconds = (datetime.utcnow() - start_time).total_seconds()
        review.status = ReviewStatus.IN_PROGRESS if review.status == ReviewStatus.PENDING else review.status

        # Store in memory
        await self.save_to_memory(
            key=f"review_{review.review_id}",
            value=review.dict(),
            context=context,
        )

        # Add to reviews index
        await self._add_to_reviews_index(review.review_id, context)

        logger.info(
            f"Completed review '{review.review_id}' "
            f"with decision: {review.approval_decision}"
        )
        return review

