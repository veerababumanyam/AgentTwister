class ReviewStatus(str, Enum):
    """Status of a review."""
    PENDING = "pending"
    IN_PROGRESS = "in_progress"
    APPROVED = "approved"
    APPROVED_WITH_WARNINGS = "approved_with_warnings"
    REJECTED = "rejected"
    NEEDS_REVISION = "needs_revision"


class SeverityLevel(str, Enum):
    """Severity level for review issues."""
    INFO = "info"
    LOW = "low"
    MEDIUM = "medium"
    HIGH = "high"
    CRITICAL = "critical"


class ReviewIssue(BaseModel):
    """An issue found during review."""

    issue_id: str = Field(default_factory=lambda: str(uuid4()))
    severity: SeverityLevel
    category: str = Field(..., description="Type of issue")
    title: str = Field(..., min_length=1, max_length=200)
    description: str = Field(..., min_length=1, max_length=1000)
    location: Optional[str] = Field(None, description="Where the issue was found")
    suggestion: Optional[str] = Field(None, description="Recommended fix")
    code_snippet: Optional[str] = Field(None, description="Relevant code snippet")


