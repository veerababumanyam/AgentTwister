    async def _generate_recommendations(
        self,
        review: PayloadReview,
        context: AgentContext,
    ) -> List[str]:
        """Generate improvement recommendations based on review."""
        recommendations = []

        # Add recommendations from stealth assessment
        if review.stealth_assessment:
            recommendations.extend(review.stealth_assessment.recommendations)

        # Add recommendations based on issues
        if review.issues:
            high_priority_issues = [
                issue for issue in review.issues
                if issue.severity in (SeverityLevel.CRITICAL, SeverityLevel.HIGH)
            ]
            if high_priority_issues:
                recommendations.append(
                    f"Address {len(high_priority_issues)} high-priority issue(s) before deployment"
                )

        # Add recommendations based on compliance
        if not review.all_compliance_passed:
            recommendations.append(
                "Resolve all compliance violations before proceeding"
            )

        # Add recommendations based on scores
        if review.correctness_score < 70:
            recommendations.append(
                "Improve payload correctness - validation errors detected"
            )

        if review.stealth_assessment and review.stealth_assessment.human_invisibility_score < 60:
            recommendations.append(
                "Improve human invisibility - current technique may be detected"
            )

        return recommendations

    def _clean_and_parse_json(self, response: str) -> Dict[str, Any]:
        """Clean and parse JSON from LLM response."""
        try:
            return json.loads(response)
        except json.JSONDecodeError:
            pass

        # Try extracting JSON from markdown code blocks
        if "```json" in response:
            json_part = response.split("```json")[1].split("```")[0].strip()
            return json.loads(json_part)
        elif "```" in response:
            json_part = response.split("```")[1].split("```")[0].strip()
            return json.loads(json_part)

        # Try finding first { and last }
        first_brace = response.find("{")
        last_brace = response.rfind("}")
        if first_brace >= 0 and last_brace > first_brace:
            json_part = response[first_brace:last_brace + 1]
            return json.loads(json_part)

        raise ValueError("Could not extract valid JSON from LLM response")

